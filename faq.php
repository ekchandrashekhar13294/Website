				<br>
				<p><b><h5> What Channel Manager Does?</b></h5></p>
				<p>The common issue among hotels is not to cooperate with a lot of OTAs such as Booking.com, Expedia or other smaller online distribution channels due to the extra time and personnel required to manage these connections. Another usual practice is to offer only a few rooms online, in order to avoid having overbooking issues or selling a high percentage of the hotel’s rooms through third parties.</p>
				<p>A channel manager system offers the solution to all these issues. By adding a Channel Manager, the hotel can directly update availability and rates to all OTAs, with or without the involvement of their staff.</p>
				
				
				<br>
				<p><b><h5> How does Channel Manager work?</b></h5></p>
				<br>
				<p>A Channel Manager connects the hotel’s booking engine, directly with all major online distribution channels. By the time a reservation is added on the Booking System of the hotel the Channel Manager automatically updates the hotel’s availability on all the connected channels. There is no need for the hotel’s personnel to log in to every single channel in order to update availability. Consequently, by using a channel manager, the hotel saves both time and money.</p>
				<p>In case that a reservation is made in one of the reservation websites (e.g. MakeMyTrip or VIA.com), the Channel Manager updates availability automatically in all the connected channels and the hotel’s booking engine</p>
				<p>The Reservation Department’s personnel can easily retrieve information of every reservation from either the connected OTAs or the Channel Manager’s Reservation Page. All the necessary information, including payment and guest details is there and the time needed for the management of each reservation is impressively reduced.
					<br>Just like availability, rates can also be seamlessly updated through a Channel Management System. The hotel is able to adjust rates through its Channel Manager and the system updates in a few seconds the rates in all the connected OTAs! That way rate parity is not an issue anymore! Of course, in case that the hotel needs to have a different pricing policy for each OTA, it is possible to set various pricing rules for every single channel.</p>
				
				
				<br>
				<p><b><h5> What is OTA (Online Travel Agency)?</b></h5></p>
				<p>OTAs are online companies who's websites allow consumers to book various travel related services directly via Internet. They are 3rd party agents reselling trips, hotels, cars, flights, vacation packages etc. provided / organised by others.
					<br>Online Travel Agency means the distribution agent from where guest will book the rooms like MakeMyTrip, VIA, Booking.com, Expedia.com, Clear-trip and many more.</p>
				
				
