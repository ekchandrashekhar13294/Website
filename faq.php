				<p>For Hotelier booking engine will be their hotel websites that we will be providing them with integrated payment gateway. The benefits of having a booking engine is to increase the direct booking from hotel websites. With booking engine hotelier can also give some attractive deal/coupon to the guest. Atithih will be providing a FREE booking engine to all the connected hoteliers</p>
				
				
